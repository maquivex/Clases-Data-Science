#FUNCIONES ANONIMAS
#Las funciones anonimas o lambda son funciones que no tienen nombre y se utilizan para operaciones simples y rápidas.
#Se definen utilizando la palabra clave 'lambda' seguida de los argumentos, dos puntos y la expresión que se desea evaluar.

#Ejemplo 1: Funcion lambda que suma dos numeros
suma = lambda x, y: x + y
resultado = suma(5, 3)
print("La suma es:", resultado)
#Salida: La suma es: 8
#Ejemplo 2: Funcion lambda que eleva un numero al cuadrado
cuadrado = lambda x: x ** 2
resultado = cuadrado(4)
print("El cuadrado es:", resultado)




